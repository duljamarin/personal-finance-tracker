const ICON_SET = new Set(ICON_PALETTE);

/** Returns the icon key for a category (pass to CategoryIconSvg or CATEGORY_ICONS[key]) */
export function getCategoryIcon(cat) {
  if (!cat) return 'Shopping';
  const stored = cat.emoji;
  if (stored && ICON_SET.has(stored)) return stored;
  const name = cat.name || cat;
  if (ICON_SET.has(name)) return name;
  return null; // null = user-created category, caller should show initial
}

/** @deprecated Use getCategoryIcon + CATEGORY_ICONS instead */
export const EMOJI_PALETTE = ICON_PALETTE;

/** @deprecated Use getCategoryIcon instead */
export function getCategoryEmoji(cat) {
  return getCategoryIcon(cat) || 'Shopping';
}

/**
 * Translates a category name if it's a default category.
 * Returns original name for user-created categories.
 */
export function translateCategoryName(categoryName) {
  if (!categoryName) return '';
  if (DEFAULT_CATEGORIES.includes(categoryName)) {
    return i18n.t(`defaultCategories.${categoryName}`);
  }
  return categoryName;
}

/**
 * Check if a category is a default one.
 */
export function isDefaultCategory(categoryName) {
  return DEFAULT_CATEGORIES.includes(categoryName);
}

